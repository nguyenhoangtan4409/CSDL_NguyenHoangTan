CREATE TABLE KHACHHANG
(
	MAKH char(4) CONSTRAINT PK_KH PRIMARY KEY,
	HOTEN varchar(40),
	DCHI varchar(50),
	SODT VARCHAR(20),
	NGSINH SMALLdatetime,
	DOANHSO money,
	NGDK SMALLdatetime
)

CREATE TABLE NHANVIEN
(
	MANV char(4) CONSTRAINT PK_NV PRIMARY KEY,
	HOTEN varchar(40) ,
	NGVL SMALLdatetime,
	SODT varchar(20),
)

CREATE TABLE SANPHAM
(
	MASP char(4) CONSTRAINT PK_SP PRIMARY KEY,
	TENSP VARCHAR(40),
	DVT VARCHAR(20),
	NUOCSX VARCHAR(40) ,
	GIA money ,
)

CREATE TABLE HOADON
(
	SOHD INT CONSTRAINT PK_HD PRIMARY KEY,
	NGHD SMALLDATETIME,
	MAKH CHAR(4),
	MANV CHAR(4),
	TRIGIA MONEY,
)

CREATE TABLE CTHD
(
	SOHD INT,
	MASP CHAR(4),
	SL INT,
	CONSTRAINT PK_CTHD PRIMARY KEY(SOHD, MASP)
)

